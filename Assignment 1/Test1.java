class Test1{
	public static void main(String args[])
	{
	 int number =30;

 
    if (number > 0) {
 
      System.out.println(number + " is positive.");
 
    } else if (number < 0) {
 
      System.out.println(number + " is negative.");
 
    } else {
 
      System.out.println(number + " is zero.");
 
    }
 
  }
}

/*Output:
30 is positive.*/